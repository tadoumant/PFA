        <h3 class="h31">Présentation Data Science</h3>
        <p class="par1">En termes généraux, la science des données est l’extraction de connaissance d’ensembles de données</p>
        <p class="par1">La science des données est un domaine interdisciplinaire qui utilise des méthodes, des processus, des algorithmes et des systèmes scientifiques pour extraire des connaissances et des idées de nombreuses données structurelles et non structurées. Elle est souvent associée aux données massives et à l'analyse des données.</p>
        <p class="par1">Elle utilise des techniques et des théories tirées de nombreux domaines dans le contexte des mathématiques, des statistiques, de l'informatique, de la théorie et des technologies de l'information.</p>
        <p class="par1">Parmi elles : les modèles probabilistes, l’apprentissage automatique, l’apprentissage statistique, la programmation informatique, l’ingénierie de données, la reconnaissance de formes, la visualisation de données, l’analytique prophétique, la modélisation d’incertitude, le stockage de données, la géovisualisation, la compression de données et le calcul à haute performance. Les méthodes qui s’adaptent aux données de masse sont particulièrement intéressantes dans la science des données, bien que la discipline ne soit généralement pas considérée comme limitée à ces données.</p>
        <p class="par1">La science des données (en anglais data science) est une discipline qui s'appuie sur des outils mathématiques, de statistiques, d’informatique (cette science est principalement une « science des données numériques ») et de visualisation des données. Elle est en plein développement, dans le monde universitaire ainsi que dans le secteur privé et le secteur public. Moore en 1991 a défini la statistique comme la science des données (définition reprise par d’autres, dont James T. McClave et al. en 19977) et U. Beck en 20018 oppose la science des données à la science de l’expérience, voyant une dissociation croissante entre ces deux types de science, que tendrait selon lui à encourager une société de la gestion du risque au sein d’une « civilisation du danger »</p>
        
        <h3 class="h31">Objectifs pédagogiques de la formation Data Science</h3>
        <p class="par1"><i class="fa fa-circle" aria-hidden="true"></i> Utiliser R pour nettoyer / analyser et visualiser les données</p>
        <p class="par1"><i class="fa fa-circle" aria-hidden="true"></i> Naviguer dans l'ensemble du pipeline de la science des données de l'acquisition des données à leur publication</p>
        <p class="par1"><i class="fa fa-circle" aria-hidden="true"></i> Utiliser GitHub pour gérer les projets de science des données</p>
        <p class="par1"><i class="fa fa-circle" aria-hidden="true"></i> Effectuer des analyses de régression / des moindres carrés et des inférences en utilisant des modèles de régression</p>

        <h3 class="h31">Public visé par la formation Data Science</h3>
        <p class="par1">La formation Data Science est destinée aux Data Miner, analyste, statisticien, ou tout acteur ayant à consolider et faire valider ses connaissances pour obtenir une certification Data Scientist</p>

        <h3 class="h31">Prérequis de la formation Data Science</h3>
        <p class="par1">Afin de pouvoir suivre le programme de la certification Data Science, il est recommandé d’avoir des connaissances de base en mathématiques et statistiques</p>

