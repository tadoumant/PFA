        <h3 class="h31">Présentation Sécurité Informatique </h3>
        <p class="par1">La spécialisation Sécurité Informatique s’adresse aux étudiants souhaitant une formation de très haut niveau sur les méthodes d’audits et de tests d’intrusion, d’identification et d’analyse avancée de malwares, de protection et de sécurisation du Système d’Information.</p>
        <p class="par1">Dans un contexte d’évolution rapide de la sécurité, cette spécialisation permet d'apprendre à identifier les menaces et les vulnérabilités. Elle forme des experts assurant la pérennité de la sécurité des systèmes, des flux et des données dans les entreprises. Les projets pédagogiques sur la détection et l’analyse de vulnérabilités, la sécurité avancée des systèmes, la détection et la prévention d’intrusions, l’analyse forensic, la sécurité offensive, ainsi que la préparation aux certifications professionnelles CEH, ITIL, et OSCP, contribuent à élargir le champ d’expertise des étudiants.</p>
        <p class="par1">Dès le cycle Mastère, les étudiants peuvent s'orienter vers une spécialisation technique ou fonctionnelle.</p>
        <p class="par1">Chaque année, les étudiants obtiennent 60 crédits ECTS.</p>
        <p class="par1">Ce cursus comprend 65 jours de cours par an.</p>

