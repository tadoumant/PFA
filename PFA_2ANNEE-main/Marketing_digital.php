        <h3 class="h31">Présentation marketing digital</h3>
        <p class="par1">Le marketing électronique, appelé aussi marketing en ligne ou marketing numérique ou par abus de langage marketing digital1, correspond à l'ensemble des méthodes et des pratiques marketing utilisées sur Internet : communication en ligne (influence et réseaux sociaux), optimisation du commerce électronique, création de trafic au travers de tous supports numériques. En 2021, l'expression marketing Digital a largement supplanté les autres telles que « marketing électronique » (environ 100 millions de recherche sur Google comptabilisées au 27 janvier 2021) ou marketing numérique (72 millions de recherche sur Google au 27 janvier 2021) puisque « Marketing digital » compte plus de 3 milliards de recherches sur Google).</p>
        <p class="par1">Le terme regroupe chaque discipline du marketing électronique (cybermarketing pour le marketing fait sur internet ; marketing mobile pour le marketing sur les terminaux portables, etc.). Il fait partie de la stratégie numérique d'une personne, d'un groupe (entreprise, lobby…).</p>
        
